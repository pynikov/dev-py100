salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
months = 10  # Количество месяцев, которое планируется протянуть без долгов
increase = 0.03  # Ежемесячный рост цен

# TODO Рассчитайте подушку безопасности, чтобы протянуть 10 месяцев без долгов

# Переменная для хранения необходимой подушки безопасности
required_money_capital = 0.0

# Рассчитываем нехватку бюджета для каждого месяца
for _ in range(months):
    # Рассчитываем нехватку средств за текущий месяц
    deficit = spend - salary
    # Если дефицит есть, добавляем его в подушку безопасности
    if deficit > 0:
        required_money_capital += deficit
    # Увеличиваем расходы на 3% на следующий месяц
    spend *= (1 + increase)

# Округляем итоговую подушку безопасности до двух знаков
required_money_capital = round(required_money_capital, 2)

# Вывод результата
print(f"Подушка безопасности, чтобы протянуть {months} месяцев без долгов:", required_money_capital)
