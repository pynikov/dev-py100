numbers = [2, -93, -2, 8, None, -44, -1, -85, -14, 90, -22, -90, -100, -8, 38, -92, -45, 67, 53, 25]

# TODO заменить значение пропущенного элемента средним арифметическим

# Отфильтровываем список, чтобы оставить только числа (исключаем None)
filtered_numbers = [num for num in numbers if num is not None]

# Вычисляем сумму отфильтрованных чисел
total_sum = sum(filtered_numbers)

# Находим общее количество элементов в исходном списке, включая None
count = len(numbers)

# Вычисляем среднее арифметическое: сумма всех чисел делится на общее количество элементов
average = total_sum / count

# Проходим по списку и заменяем пропущенный элемент (None) на вычисленное среднее арифметическое
numbers = [average if num is None else num for num in numbers]

# Выводим изменённый список
print("Измененный список:", numbers)
